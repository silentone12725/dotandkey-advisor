"""
backend/playbooks/recommend.py

Runs graph retrieval against the user's profile, then streams a SHORT
intro line and attaches structured product cards — top 3 highlighted
with deterministic "why this matches" keyword tags (see
backend/match_keywords.py), remaining shown plainly.

Also retrieves matching combo deals and collaborative-filter picks from
the user preference graph, which are prepended to the response so the
widget can show them before the individual top picks.

Per-product reasoning used to be LLM-written prose (1-2 sentences each),
which produced long, repetitive chat bubbles on live testing. That's
been replaced by graph-derived keyword tags rendered directly on each
card — faster, can't hallucinate, and far more scannable. The LLM's only
remaining job here is one short opening line.

Keyword inputs (matched concerns, texture, ingredients, free_from) come
straight off result.top_picks — retrieval.py's query already collects
them for every product in every fallback round, so no second graph
round-trip is needed here.

Also records R (recommended) events in the history store for
every product in top_picks.
"""

